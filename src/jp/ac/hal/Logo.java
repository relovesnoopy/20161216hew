package jp.ac.hal;

public class Logo {
	private int logoid;
	private String logoname;
	private String logopass;
	private int logoprice;
	public int getLogoid() {
		return logoid;
	}
	public void setLogoid(int logoid) {
		this.logoid = logoid;
	}
	public String getLogoname() {
		return logoname;
	}
	public void setLogoname(String logoname) {
		this.logoname = logoname;
	}
	public String getLogopass() {
		return logopass;
	}
	public void setLogopass(String logopass) {
		this.logopass = logopass;
	}
	public int getLogoprice() {
		return logoprice;
	}
	public void setLogoprice(int logoprice) {
		this.logoprice = logoprice;
	}
}
